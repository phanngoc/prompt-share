import { Badge } from '@/components/ui/badge'

interface Feature {
  label: string
  value?: string
  icon?: string
}

interface PromptFeaturesProps {
  features: Feature[]
}

export function PromptFeatures({ features }: PromptFeaturesProps) {
  return (
    <div className="flex flex-wrap gap-2">
      {features.map((feature, index) => (
        <Badge
          key={index}
          variant="secondary"
          className="bg-gray-100 text-gray-800 flex items-center gap-1"
        >
          {feature.icon && <FeatureIcon name={feature.icon} />}
          <span>{feature.label}</span>
          {feature.value && <span className="text-gray-500">{feature.value}</span>}
        </Badge>
      ))}
    </div>
  )
}

const FeatureIcon = ({ name }: { name: string }) => {
  switch (name) {
    case 'check':
      return <CheckIcon className="w-4 h-4" />
    case 'book':
      return <BookIcon className="w-4 h-4" />
    case 'image':
      return <ImageIcon className="w-4 h-4" />
    case 'sparkles':
      return <SparklesIcon className="w-4 h-4" />
    case 'star':
      return <StarIcon className="w-4 h-4" />
    case 'user':
      return <UserIcon className="w-4 h-4" />
    default:
      return null
  }
}

